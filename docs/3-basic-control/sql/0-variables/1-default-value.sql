-- ============================================================
-- デフォルト値付きの変数
-- ============================================================
-- 宣言時に := で初期値を設定できる

DO $$
DECLARE
    v_status VARCHAR(20) := 'active';
    v_limit  INT         := 10;
    v_rate   NUMERIC     := 0.08;
BEGIN
    RAISE NOTICE 'ステータス: %, 上限: %, 率: %', v_status, v_limit, v_rate;
END;
$$;
